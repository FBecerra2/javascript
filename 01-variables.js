//var esto permite redeclarar la variable y reasignarle un valor a la variable cuantas veces quiera. Hoy en dia se recomienda no usar var.
var helloWorld = 'Hello World';
console.log(helloWorld);

//let esto permite redeclarar la variable pero no reasignarle un valor a la variable cuantas veces quiera. Se asigna dentro de un bloque de codigo.

var helloWorld2 = 'Hello World 2';
console.log(helloWorld2);

//const esto no permite redeclarar la variable ni reasignarle un valor a la variable cuantas veces quiera. Se asigna dentro de un bloque de codigo.

const helloWorld3 = 'Hello World 3';
console.log(helloWorld3);
